let rowLength = 0;
data = data.split("\n").map((v) => {
  const arr = v.split("");
  rowLength = arr.length;
  return arr.map((p) => (p === "." ? false : true));
});

let steps = [
  [1, 1],
  [1, 3],
  [1, 5],
  [1, 7],
  [2, 1],
];
let totalPathArr = []

steps.forEach(step => {
  let pathArr = [];
  let currRowIdx = 0;
  for (let index = 0; index < data.length; index+=step[0]) {
    const row = data[index];
    pathArr.push(row[currRowIdx]);
    for (let _i = 1; _i <= step[1]; _i++) {
      currRowIdx++;
      if (currRowIdx > rowLength - 1) {
        currRowIdx = 0;
      }
    }
  }
  totalPathArr.push(pathArr.filter((v) => v).length);
});

console.log(totalPathArr.reduce((acc,n)=>acc*n,1));
